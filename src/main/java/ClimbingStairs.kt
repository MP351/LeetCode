class ClimbingStairs {
    fun climbStairs(n: Int): Int {
        val n1 = 1
        val n2 = 2

        if (n == n1) {
            return n1
        }
        if (n == n2) {
            return n2
        }

        return calc(n, 3, n1, n2)
    }

    private fun calc(n: Int, step: Int, n1: Int, n2: Int): Int {
        if (step == n) {
            return n1 + n2
        }

        return calc(n, step+1, n2, n1 + n2)
    }

